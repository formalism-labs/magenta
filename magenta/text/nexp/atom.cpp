
#include "atom.h"
#include "node.h"

#include <stdio.h>

namespace magenta
{

using std::cout;
using std::endl;
	
///////////////////////////////////////////////////////////////////////////////////////////////
// Atom
///////////////////////////////////////////////////////////////////////////////////////////////

Atom::Atom(long n, int line) : Node(line)
{ 
	char s[20]; 
	sprintf(s, "%ld", n); 
	_text = s; 
}

//---------------------------------------------------------------------------------------------

Atom::Atom(double n, int line) : Node(line)
{ 
	char s[20]; 
	sprintf(s, "%lf", n); 
	_text = s; 
}

//---------------------------------------------------------------------------------------------

void Atom::print() const
{
	cout << _text.c_str() << endl;
}

///////////////////////////////////////////////////////////////////////////////////////////////
	
} // namespace magenta

