#ifndef _magenta_system_win32_language_
	#define _magenta_system_win32_language_

#include <string>

#ifndef _WINDOWS_
#include <windows.h>
#endif

namespace magenta
{

using std::string;

///////////////////////////////////////////////////////////////////////////////////////////////

class Language
{
	WORD _language;

public:
	Language(int lang = LANG_NEUTRAL, int subLang = SUBLANG_NEUTRAL) : 
		_language(MAKELANGID(lang, subLang)) {}

	operator WORD() const { return _language; }
};

///////////////////////////////////////////////////////////////////////////////////////////////

} // namespace magenta

#endif // _magenta_system_win32_language_
